return function(_, level_width, level_height)
   assertx.is_number(level_width)
   assertx.is_number(level_height)

   if level_width ~= 32 or level_height ~= 32 then
      error("this generation works only a size 32x32 level")
   end

   local map = [[
................................
................................
................................
..............##................
..............##................
................................
................................
................................
................................
######....................######
................................
................................
................................
................................
................................
......##..##..##..##..##........
................................
................................
................................
...........#......#.............
...........#......#.............
...........#......#.............
...........#......#.............
................................
................................
................................
................................
................................
................................
................................
................................
................................
   ]]

   local matrix = ds.Matrix()

   local x
   local y = 0
   for line in map:gmatch("%S+") do
      x = 0
      y = y + 1
      for char in line:gmatch("%S") do
         x = x + 1
         if char == "." then
            matrix:set(ds.Point.get(x, y), false)
         elseif char == "#" then
            matrix:set(ds.Point.get(x, y), true)
         else
            error("unknown character in preset map")
         end
      end
   end

   return matrix
end

